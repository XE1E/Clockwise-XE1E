def rgb565_to_rgb888(color):
    """Convierte RGB565 a RGB888"""
    r = ((color >> 11) & 0x1F) * 255 // 31
    g = ((color >> 5) & 0x3F) * 255 // 63
    b = (color & 0x1F) * 255 // 31
    return (r, g, b)

def create_image(pixels, width, height, transparent_color=None):
    """Crea imagen PIL desde array de pixeles RGB565"""
    if transparent_color is not None:
        img = Image.new('RGBA', (width, height), (0, 0, 0, 0))
    else:
        img = Image.new('RGB', (width, height))

    for y in range(height):
        for x in range(width):
            idx = y * width + x
            if idx < len(pixels):
                color = pixels[idx]
                if transparent_color is not None and color == transparent_color:
                    if img.mode == 'RGBA':
                        img.putpixel((x, y), (0, 0, 0, 0))
                else:
                    rgb = rgb565_to_rgb888(color)
                    if img.mode == 'RGBA':
                        img.putpixel((x, y), (*rgb, 255))
                    else:
                        img.putpixel((x, y), rgb)
    return img

